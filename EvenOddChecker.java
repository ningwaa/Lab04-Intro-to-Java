public class EvenOddChecker {

    public static void main(String[] args) {
        int num = 2;
        if (num % 2 == 0) //this formula is calculating even or odd entries
        {
            System.out.println(num + " = even ");
        }
        else
        {
            System.out.println(num + " = odd ");
        }

    }
}
